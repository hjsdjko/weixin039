			youqinglianjietuTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.youqinglianjietu = 'file/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
